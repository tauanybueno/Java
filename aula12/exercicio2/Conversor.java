public class Conversor {

    public static double metrosParaMilimetros (double x){
        return x * 1000;
    }

    public static double metrosParaCentimetros (double x){
        return x * 100;
    }

    public static double metrosParaDecimetros (double x){
        return x * 10;
    }

    public static double milimetrosParaMetros (double x){
        return x / 1000;
    }

    public static double centimetrosParaMetros (double x){
        return x / 100;
    }

    public static double decimetrosParaMetros (double x){
        return x / 10;
    }
}
